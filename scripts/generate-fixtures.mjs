// Generates src/mocks/fixtures/tickets.ts from real ZD JSON dumps.
// Run: node scripts/generate-fixtures.mjs

import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ZD_DIR = '/Users/guilhermeco/Desktop/Stream/fetch_from_zd'
const OUT = resolve(__dirname, '../src/mocks/fixtures/tickets.ts')

const read = (file) => JSON.parse(readFileSync(resolve(ZD_DIR, file), 'utf8'))

function deriveTier(tags) {
  if (tags.some(t => t.includes('enterprise'))) return 'enterprise'
  if (tags.some(t => t === 'free' || t.includes('_free'))) return 'free'
  return 'pro'
}

function deriveHoldType(tags, status) {
  if (status !== 'hold') return null
  if (tags.includes('feature_request_v2')) return 'feature_request'
  return 'linear'
}

function deriveCustomer(orgId) {
  if (!orgId) return 'Unknown'
  return 'Org-' + String(orgId).slice(-5)
}

function mapTicket(t) {
  const status = (t.status === 'closed' || t.status === 'new') ? 'open' : t.status
  return {
    id:        t.id,
    subject:   t.subject,
    status,
    priority:  t.priority ?? 'normal',
    tier:      deriveTier(t.tags),
    holdType:  deriveHoldType(t.tags, status),
    tags:      t.tags.filter(tag =>
      !['1', 'false', 'true', 'slack_notified', 'thena'].includes(tag)
    ),
    updatedAt: new Date(t.updated_at).getTime(),
    replies:   0,
    sentiment: null,
    assignee:  t.assignee_id ? 'GC' : '',
    linear:    null,
    customer:  deriveCustomer(t.organization_id),
  }
}

const open        = read('tickets_open.json').map(mapTicket)
const pending     = read('tickets_pending.json').map(mapTicket)
const hold        = read('tickets_hold.json').map(mapTicket)
const newUn       = read('tickets_new_unassigned.json').map(mapTicket)

// Only keep solved tickets from the last 7 days (board only shows recent solved)
const sevenDays   = Date.now() - 7 * 24 * 3600_000
const solved      = read('tickets_solved.json')
  .map(mapTicket)
  .filter(t => t.updatedAt > sevenDays)

const all = [...open, ...pending, ...hold, ...solved, ...newUn]

console.log(`Mapped: ${open.length} open, ${pending.length} pending, ${hold.length} hold, ${solved.length} solved (7d), ${newUn.length} new/unassigned`)
console.log(`Total: ${all.length} tickets`)

const output = `// Auto-generated by scripts/generate-fixtures.mjs — do not edit manually.
// Run \`node scripts/generate-fixtures.mjs\` to refresh from ZD data.
import type { Ticket } from '../../types/ticket'

export const SEED_TICKETS: Ticket[] = ${JSON.stringify(all, null, 2)}
`

writeFileSync(OUT, output)
console.log(`Written → ${OUT}`)
